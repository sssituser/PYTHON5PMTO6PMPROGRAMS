def sub(num1,num2): #defining the function
    return num1-num2  
def mul(num1,num2):
    return num1*num2
def div(num1,num2):
    return num1//num2
def rem(num1,num2):
    return num1%num2
def expo(base,power):
    return base**power

print(sub(5,2))
print(mul(5,2))
print(div(5,2))
print(rem(5,2))
print(expo(5,2))

